namespace FreiService.Computus;

/// <summary>
/// Represents liturgical colors used in The Lutheran Hymnal (TLH) tradition.
/// </summary>
public enum LiturgicalColor
{
    /// <summary>
    /// White: Christmas season, Epiphany, Easter season (through Ascension), Trinity Sunday, Christ-centered feasts.
    /// </summary>
    White,

    /// <summary>
    /// Red: Pentecost, Reformation, martyrs, Confirmation, ordination.
    /// </summary>
    Red,

    /// <summary>
    /// Violet/Purple: Advent, Lent (except feasts), Passiontide.
    /// </summary>
    Violet,

    /// <summary>
    /// Black: Good Friday, funerals (optional).
    /// </summary>
    Black,

    /// <summary>
    /// Green: Epiphany season (after Epiphany 1), Trinity season.
    /// </summary>
    Green
}
