namespace FreiService.Computus;

/// <summary>
/// Represents the liturgical seasons in the Confessional Lutheran church calendar.
/// </summary>
public enum ChurchSeason
{
    /// <summary>
    /// The season of Advent, beginning four Sundays before Christmas and preparing for the coming of Christ.
    /// </summary>
    Advent,

    /// <summary>
    /// The Christmas season, from Christmas Day through the Eve of Epiphany (January 5).
    /// </summary>
    Christmas,

    /// <summary>
    /// The season of Epiphany, from January 6 until Ash Wednesday.
    /// </summary>
    Epiphany,

    /// <summary>
    /// The season of Lent, from Ash Wednesday through the day before Easter (Holy Saturday).
    /// </summary>
    Lent,

    /// <summary>
    /// The Easter season, from Easter Sunday through the day before Pentecost.
    /// </summary>
    Easter,

    /// <summary>
    /// The season after Pentecost, also known as Trinity season, from Pentecost through the day before Advent.
    /// </summary>
    Pentecost
}
